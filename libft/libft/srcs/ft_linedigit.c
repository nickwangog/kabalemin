#include "libft.h"

int		ft_linedigit(char *s)
{
	int	i;

	i = -1;
	if (ft_isdigit(s[0]))
	{
		while (ft_isdigit(s[++i]))
			;
		return (s[i] == '\0');
	}
	else
		return (0);
}
